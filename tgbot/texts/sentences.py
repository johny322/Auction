async def get_profile_text(user: User, user_status: UserStatus, user_type: UserType) -> str:
    if user_type.name == UserTypeStr.boy:
        user_type_text = '–º—É–∂—á–∏–Ω–∞ üë±‚Äç‚ôÇÔ∏è'
    else:
        user_type_text = '–¥–µ–≤—É—à–∫–∞ üë±‚Äç‚ôÄÔ∏è'
    status_text = get_status_text(user_status.name)

    subscription_expiration = user.subscription_expiration
    if get_now_datetime() > subscription_expiration:
        subscription_expiration_text = '–æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç'
    else:
        subscription_expiration_text = '–¥–µ–π—Å—Ç–≤—É–µ—Ç –¥–æ {}'.format(
            subscription_expiration.strftime(TEMPLATE_DATE_FORMAT)
        )
    text = f'–¢–∏–ø –∞–∫–∫–∞—É–Ω—Ç–∞: {user_type_text}\n' \
           f'–°—Ç–∞—Ç—É—Å –∞–∫–∫–∞—É–Ω—Ç–∞: {status_text}\n' \
           f'–ü–æ–¥–ø–∏—Å–∫–∞: {subscription_expiration_text}'
    return text


def get_status_text(status: str) -> str:
    if status == UserStatusStr.gold:
        status_text = '–∑–æ–ª–æ—Ç–æ–π üü°'
    elif status == UserStatusStr.diamond:
        status_text = '–±—Ä–∏–ª–∏–∞–Ω—Ç–æ–≤—ã–π üíé'
    else:
        status_text = '–æ–±—ã—á–Ω—ã–π üü¢'
    return status_text


def fill_template(data: RegisterData, blur=False, hide_mobile_phone=True) -> str:
    data = data.escape_all()
    wa = '–µ—Å—Ç—å' if data.wa else '–Ω–µ—Ç'
    if data.tg:
        tg = '@' + data.tg if not data.tg.startswith('@') else data.tg
    else:
        tg = '–Ω–µ—Ç'
    years = relativedelta.relativedelta(datetime.datetime.now().date(), data.birthday).years
    if blur:
        mobile_phone = f'<tg-spoiler>–Ω–æ–º–µ—Ä —Å–∫—Ä—ã—Ç –¥–ª—è –≤–∞—Å</tg-spoiler>'
        tg = f'<tg-spoiler>telegram —Å–∫—Ä—ã—Ç –¥–ª—è –≤–∞—Å</tg-spoiler>'
        extra_info = '\n\n' \
                     f'<b>{need_sub_for_info_text}</b>'
    else:
        mobile_phone = f'<code>{data.mobile_phone}</code>'
        extra_info = ''
    rows = [
        f"–ò–º—è: {data.name}",
        f"–ü–æ–ª–Ω–æ–µ –∏–º—è: {data.full_name}",
        f"–°—Ç—Ä–∞–Ω–∞: {data.country}",
        f"–ì–æ—Ä–æ–¥: {data.city_full_name}",
        f"–í–æ–∑—Ä–∞—Å—Ç: {years}",
        f"–û —Å–µ–±–µ: {data.about}",
        f"–†–∞–∑–º–µ—Ä –≥—Ä—É–¥–∏: {data.breast_size}",
        f"–†–æ—Å—Ç: {data.height} —Å–º",
        f"–í–µ—Å: {data.weight} –∫–≥",
    ]
    if not hide_mobile_phone:
        rows.append(
            f"–ù–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞: {mobile_phone}",
        )
    rows.extend(
        [
            f"Whatsapp: {wa}",
            f"Telegram: {tg}" + extra_info
        ]
    )
    text = '\n'.join(rows)
    return text
